package br.edu.ufba.softvis.visminer.model.business;

public class Label {

  private int id;
  private String color;
  private String name;

  public Label(){}

  /**
   * @param id
   * @param color
   * @param name
   */
  public Label(int id, String color, String name) {
    super();
    this.id = id;
    this.color = color;
    this.name = name;
  }
  /**
   * @return the id
   */
  public int getId() {
    return id;
  }
  /**
   * @param id the id to set
   */
  public void setId(int id) {
    this.id = id;
  }
  /**
   * @return the color
   */
  public String getColor() {
    return color;
  }
  /**
   * @param color the color to set
   */
  public void setColor(String color) {
    this.color = color;
  }
  /**
   * @return the name
   */
  public String getName() {
    return name;
  }
  /**
   * @param name the name to set
   */
  public void setName(String name) {
    this.name = name;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + id;
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    Label other = (Label) obj;
    if (id != other.id)
      return false;
    return true;
  }	

}